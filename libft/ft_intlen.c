#include "libft.h"

int			ft_intlen(int nbr)
{
	int		i;

	i = 1;
	if (nbr < 0)
	{
		i++;
		nbr *= (-1);
	}
	while (nbr >= 10)
	{
		i++;
		nbr = (nbr / 10);
	}
	return (i);
}
